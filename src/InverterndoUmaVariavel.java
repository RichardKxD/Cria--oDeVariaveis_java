public class InverterndoUmaVariavel {
    public static void main(String[] args) {
        boolean homem = true;
        homem = !homem;
        System.out.println(homem); // Conseguimos inverter o valor da variável homem, que era true e agora é false.

        System.out.println(!homem); // Conseguimos inverter o valor da variável homem, que era false e agora é true.

    }

}
